package loop_working;

public class TableofFive {
    public static void main(String[] args) {
        //SYNTEX: for(initialization'condition'increment){
        //        //block of statements
        //   }
         int number=5;

         for(int j=1; j<=10; j++){
            System.out.println( number +"*"+j+"=" + number *j);
        }

    }
}
