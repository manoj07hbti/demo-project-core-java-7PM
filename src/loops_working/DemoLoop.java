package loops_working;

public class DemoLoop {

    public static void main(String[] args) {

  // SYNTAX :   for(initialization, condition, increment) {
        //	            //block of statements
        //	    }

        for(int i=3; i<=56; i++){

            System.out.println("Hello..."+i);
        }


        for(int i=10; i>0; i-- ){

            System.out.println("Decrement Loop "+i);
        }

    }
}
