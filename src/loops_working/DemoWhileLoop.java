package loops_working;

public class DemoWhileLoop {
    public static void main(String[] args) {

        int i = 1;

        while (i <= 10) {

            System.out.println("Demo Increment while loop : " +i);
            i++;
        }

        int j =5;

        while (j > -1){

            System.out.println("Demo Decrement while loop : " +j);
            j--;
        }
    }
}
