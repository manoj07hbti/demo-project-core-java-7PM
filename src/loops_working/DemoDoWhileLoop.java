package loops_working;

public class DemoDoWhileLoop {
    public static void main(String[] args) {

        int i = 1;

        do {

            System.out.println("Demo Do While loop Increment : " + i);
            i++;
        } while (i <= 10);


        int j = 15;

        do {

            System.out.println("Demo Do While loop Decrement : " + j);
            j--;
        } while (j > 5);
    }
}
