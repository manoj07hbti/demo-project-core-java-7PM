package Loop;

public class AssigmentWhileloop {
    public static void main(String[] args) {

        int number = 100, i = 1;
        while (i <= number) {
            if (i % 2 == 0) {
                System.out.println("Even number" + i);

            }
            i++;
        }
        int num = 100, j = 1;
        while (j <= num) {
            if (!(j % 2 == 0)) {
                System.out.println("odd number" + j);
            }
            j++;


        }

    }

}



























