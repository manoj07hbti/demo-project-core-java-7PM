package Loop_working;

public class IncreamentAndDecrement_Loop_3feb {
    public static void main(String[] args) {



   // SYNTAX :   for(initialization, condition, increment) {
    // 	            //block of statements
    // 	    }

    for(int i=1; i<=5; i++){
        System.out.println("this is increment loop " +i);
    }
    for(int i=5; i>2;  i--){
        System.out.println("this is drecrement loop " +i);
    }

    }
}
