package For_Loops.Demo_For;

public class Demowhile {
    public static void main(String[] args) {

        int i = 1;
        int b = 100;
        while (i < b) {
            if (i % 2 == 0) {
                System.out.println("even number is" + i);
                i++;
            }
        }
    }


}
