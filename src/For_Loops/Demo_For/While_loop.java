package For_Loops.Demo_For;

public class While_loop {
    public static void main(String[] args) {

       int i=0;
       do {
           System.out.println("this is my loop : " +i);
       i++;
       }
       while (i<7);

       int j=10;
       do {
           System.out.println("decrement of loop : " +j);
           j--;
       }
       while (j>0);
        }
    }
