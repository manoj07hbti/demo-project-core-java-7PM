package condition_statment;

public class Switch_Statement {

    public static void main (String[]args){


        String months = "Jul";

        switch (months){

            case "Jan":
                System.out.println("Months First : January");
                break;
            case "Feb":
                System.out.println("Months Second  : February");
                break;
            case "Mar":
                System.out.println("Months Third  : March");
                break;
            case "Apr":
                System.out.println("Months Fourth : April");
                break;
            case "Ma":
                System.out.println("Months Five : MAy");
                break;
            case "Jun":
                System.out.println("Months Six : June");
                break;
            case "Jul":
                System.out.println("Months Seven : July");
                break;
            case "Aug":
                System.out.println("Moths Eight : August");
                break;
            case "Sep":
                System.out.println("Months Nine : September");
                break;
            case "Oct":
                System.out.println("Months Ten : October");
                break;
            case "Nov":
                System.out.println("Months Eleven : November");
                break;
            case "Dec":
                System.out.println("Months Twelve : December");
                break;
            default:
                System.out.println("Invalid Months Entered");

        }

    }
}












