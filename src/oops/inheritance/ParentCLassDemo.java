package oops.inheritance;

public class ParentCLassDemo {
    //declare the method
    public void A1(){
        System.out.println("this is A1 method of class Parent");
    }

    private void A2(){
        System.out.println("this is A2 method of Parent Class");
    }
    protected void A3(){
        System.out.println("this is A3 method of Parent class");
    }


}
